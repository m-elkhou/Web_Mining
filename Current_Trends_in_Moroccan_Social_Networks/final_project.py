def normalizeArabic(text):
    text = re.sub("[Ø¥Ø£Ù±Ø¢Ø§]", "Ø§", text)
    text = re.sub("Ù‰", "ÙŠ", text)
    text = re.sub("Ø¤", "Ø¡", text)
    text = re.sub("Ø¦", "Ø¡", text)
    return(text)

# from https://alraqmiyyat.github.io/2013/01-02.html
def deNoise(text):
    noise = re.compile(""" Ù‘    | # Tashdid
                             ÙŽ    | # Fatha
                             Ù‹    | # Tanwin Fath
                             Ù    | # Damma
                             ÙŒ    | # Tanwin Damm
                             Ù    | # Kasra
                             Ù    | # Tanwin Kasr
                             Ù’    | # Sukun
                             Ù€     # Tatwil/Kashida
                         """, re.VERBOSE)
    text = re.sub(noise, '', text)
    return text

def correct_ar(text):
    text = moroccan_dialect(text)
    text = moroccan_dialect2(text)
    text = normalizeArabic(text)
    text = deNoise(text)
    
    return text

